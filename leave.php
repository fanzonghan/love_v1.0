            function Mail_sent(qq, name, text, ip, attr) {
                let host = window.location.hostname;
                $.ajax({
                    url: "admin/Mail_sent.php",
                    type: "POST",
                    data: {
                        mail_qq: stripHtmlTags(qq),
                        mail_name: stripHtmlTags(name),
                        mail_text: stripHtmlTags(text),
                        mail_ip: stripHtmlTags(ip),
                        mail_attr: stripHtmlTags(attr),
                        mail_sw: "待审核",
                        url: host,
                        sending: true
                    },
                    timeout: 5000,
                    dataType: "text",
                    success: function (res) {
                        if (res == "success") {
                            toastr["success"]("邮件发送成功 已通知管理员审核留言", "LG_NewUi");
                        } else if (res == "fail") {
                            toastr["error"]("邮件发送失败 邮件信息配置有误", "LG_NewUi");
                        }
                    }

                });
            }

            function stripHtmlTags(htmlString) {
                var strippedText = htmlString.replace(/<[^>]+>/g, '');
                return strippedText;
            }

